map_to_negativize(source_array)
  new_arr = []
  i = 0 
  
  while i < source_array.length 
  ele = source_array[i]
  new_arr.push(ele*-1)
  i += 1
  end

  new_arr
end

map_to_no_change(source_array)
  new_arr = []
  i = 0 
  
  while i < source_array.length 
  ele = source_array[i]
  new_arr.push(ele)
  i += 1
  end

  new_arr
end
 

